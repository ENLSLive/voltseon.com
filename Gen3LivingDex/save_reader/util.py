# function which converts decimal value 
# to hexadecimal value 
def to_hex(decimal): 
  hexadecimal = '' 
  while(decimal > 0): 
    remainder = decimal % 16
    hexadecimal = CONVERSION_TABLE[remainder] + hexadecimal 
    decimal = decimal // 16

  return hexadecimal

def to_bin(decimal, length=8): 
  binary = '' 
  while(decimal > 0): 
    remainder = decimal % 2
    binary = str(remainder) + binary 
    decimal = decimal // 2

  return binary.zfill(length)

def check_flag(bit, flag):
  return (flag & (1 << bit)) != 0

def split_bits(byte):
  # Ensure it's a 32-bit number
  byte &= 0xFFFFFFFF  # Mask to 32 bits
  # Extract the top 16 bits
  top_16_bits = (byte >> 16) & 0xFFFF
  # Extract the remainder (lower 16 bits)
  lower_16_bits = byte & 0xFFFF
  return top_16_bits, lower_16_bits